"""
Phase 37c — Aut(E)-projection residual test on the Phase-37 Gowers signal.

Phase 37 found a persistent ~5–10σ U^2 elevation on f_dj(k) := exp(2πi·digit_j(z(δ(k)))/p)
across the entire secp-family p sweep, with U^3 dead. The hypothesis is
that the entire U^2 elevation is the Aut(E)-equivariance artifact already
documented in Phase 21b — the rank deficit of (g_ord-1)/|Aut| in the
leading digit, which forces certain Fourier coefficients of f_dj to be
non-zero.

Test:
  1. Find the explicit Aut(E) action on the cyclic group <G>: each
     ζ ∈ Aut(E) sends G to some m_ζ·G, so it acts on indices by
     k → m_ζ·k mod g_ord.
  2. Build the orbit decomposition of {1,..,g_ord-1} under <{m_ζ}>.
  3. Project f_dj onto the orbit-constant subspace (= average over each
     orbit).  Call the projection P_aut · f_dj.
  4. Residual r := f_dj − P_aut·f_dj.
  5. Compute |r|_{U^2}, |r|_{U^3}, and max Fourier coefficient of r.
     Compare to a random S^1 baseline of the same length.

Prediction: residual U^2 collapses to random level. If so, Phase 37
detects nothing beyond Aut equivariance and the angle is closed.

(For j=0 curves with p ≡ 1 mod 3, |Aut| = 6 generated by [ω]:(x,y)→(ωx,y)
and [-1]:(x,y)→(x,-y).  For p ≡ 2 mod 3 only [-1] survives; |Aut|=2.)
"""
