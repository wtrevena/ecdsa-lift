# ── 1. Identify dominant Fourier modes ──────────────────────────────

def top_fourier_modes(f, top_k=5):
    """Return the top-k Fourier frequencies by magnitude."""
    g = np.asarray(f, dtype=complex)
    n = len(g)
    F = np.fft.fft(g) / n
    mags = np.abs(F)
    idxs = np.argsort(mags)[::-1][:top_k]
    return [(int(idx), float(mags[idx])) for idx in idxs]


# ── 2. Algebraic invariants ─────────────────────────────────────────

def curve_invariants(p, b):
    """Return algebraic invariants of y²=x³+b over F_p."""
    E_p = Curve(a=0, b=b, N=p)
    n = naive_order(E_p)
    G, g_ord = find_generator(E_p)
    a_p = p + 1 - n
    # CM: for j=0, Aut generated by [ω]:(x,y)→(ωx,y) where ω³=1
    omega = None
    omega_index = None
    if p % 3 == 1:
        for g in range(2, p):
            w = pow(g, (p - 1) // 3, p)
            if w != 1 and pow(w, 3, p) == 1:
                omega = w
                break
        if omega is not None:
            Gx, Gy = G
            wG = (omega * Gx % p, Gy)
            cur = G
            for m in range(1, g_ord):
                if cur == wG:
                    omega_index = m
                    break
                cur = E_p.add(cur, G)
    return {
        "p": p, "b": b, "n": n, "g_ord": g_ord, "a_p": a_p,
        "omega": omega, "omega_index": omega_index, "G": G,
    }


# ── Main investigation ──────────────────────────────────────────────

def investigate_prime(p, b, e=4):
    """Full investigation for one (p, b, e)."""
    inv = curve_invariants(p, b)
    g_ord = inv["g_ord"]
    seq, _, N = z_delta_sequence(p, b, e, max_k=g_ord - 1)
    n = len(seq)
    if n < 8:
        return {"p": p, "status": "too small"}

    result = dict(inv)
    result["e"] = e
    result["N"] = N
    result["seq_len"] = n

    digit_modes = {}
    for j in range(1, e):
        f = [encode_digit(z, p, j) for z in seq]
        modes = top_fourier_modes(f, top_k=5)
        digit_modes[f"d{j}"] = {
            "top_freq": modes[0][0],
            "top_mag": modes[0][1],
            "second_freq": modes[1][0],
            "second_mag": modes[1][1],
            "top5": modes,
            "peak_ratio": modes[0][1] / modes[1][1] if modes[1][1] > 0 else float('inf'),
        }
        # Derived: what is top_freq as a fraction of g_ord?
        dm = digit_modes[f"d{j}"]
        alpha = dm["top_freq"]
        dm["alpha_over_n"] = alpha / n
        dm["alpha_over_g_ord"] = alpha / g_ord if g_ord > 0 else None
        # Check: is alpha related to omega_index?
        if inv["omega_index"] is not None:
            dm["alpha_mod_omega_idx"] = alpha % inv["omega_index"]
            dm["alpha_times_omega_idx_mod_n"] = (alpha * inv["omega_index"]) % n
        # Check: alpha * a_p mod n
        dm["alpha_times_ap_mod_n"] = (alpha * inv["a_p"]) % n

    result["digit_modes"] = digit_modes
    return result


def precision_sweep(p, b, e_range=(2, 3, 4, 5, 6)):
    """Check how the peak magnitude changes with p-adic precision e."""
    inv = curve_invariants(p, b)
    g_ord = inv["g_ord"]
    results = []
    for e in e_range:
        try:
            seq, _, N = z_delta_sequence(p, b, e, max_k=g_ord - 1)
        except Exception:
            results.append({"e": e, "status": "failed"})
            continue
        n = len(seq)
        if n < 8:
            results.append({"e": e, "status": "too small"})
            continue
        row = {"e": e, "n": n}
        for j in range(1, e):
            f = [encode_digit(z, p, j) for z in seq]
            modes = top_fourier_modes(f, top_k=3)
            row[f"d{j}_freq"] = modes[0][0]
            row[f"d{j}_mag"] = modes[0][1]
        results.append(row)
    return results


def non_cm_test(p_list):
    """Test the anomaly on non-j=0 curves:
       y²=x³+x (j=1728, CM by Z[i])
       y²=x³+x+1 (generic, no CM)
    """
    curves = [
        ("j0_b7", 0, 7),       # j=0 control
        ("j1728", 1, 0),        # j=1728, CM by Z[i]  (y²=x³+x)
        ("generic", 1, 1),      # no CM (y²=x³+x+1)
    ]
    results = {}
    for label, a_coeff, b_coeff in curves:
        results[label] = []
        for p in p_list:
            E_p = Curve(a=a_coeff, b=b_coeff, N=p)
            try:
                n = naive_order(E_p)
            except Exception:
                continue
            if n <= 4 or n % p == 0:
                continue
            G, g_ord = find_generator(E_p)
            if g_ord < 10:
                continue
            N = p ** 4
            E_aff = Curve(a=a_coeff, b=b_coeff, N=N)
            from src.projective import ProjCurve
            from src.lifts import teichmuller_lift
            C = ProjCurve(a=a_coeff, b=b_coeff, N=N)
            # Build z(δ(k)) manually since z_delta_sequence assumes a=0
            from src.curves import modinv
            tau_G = C.from_affine(teichmuller_lift(E_p, E_aff, G))
            seq = []
            for k in range(1, g_ord):
                kG_fp = E_p.mul(k, G)
                tau_kG = C.from_affine(teichmuller_lift(E_p, E_aff, kG_fp))
                diff = C.add(C.mul(k, tau_G), C.neg(tau_kG))
                X, Y, Z = diff
                try:
                    z = (-X * modinv(Y % N, N)) % N
                except ZeroDivisionError:
                    z = None
                seq.append(z)
            n_seq = len(seq)
            if n_seq < 10:
                continue
            # Compute Fourier anomaly for digit 1
            f = [encode_digit(z, p, 1) for z in seq]
            modes = top_fourier_modes(f, top_k=3)
            # Random baseline
            perm_mags = []
            for _ in range(80):
                perm = np.random.permutation(n_seq)
                f_perm = np.array(f)[perm]
                pm = top_fourier_modes(f_perm.tolist(), top_k=1)
                perm_mags.append(pm[0][1])
            perm_arr = np.array(perm_mags)
            z_score = (modes[0][1] - perm_arr.mean()) / perm_arr.std() if perm_arr.std() > 0 else 0
            results[label].append({
                "p": p, "n": n_seq, "g_ord": g_ord,
                "top_freq": modes[0][0], "top_mag": modes[0][1],
                "perm_mean": float(perm_arr.mean()),
                "perm_std": float(perm_arr.std()),
                "z_vs_perm": round(z_score, 2),
            })
    return results


def scaling_law(data_points):
    """Fit log(peak_mag) = -β·log(n) + c via least squares."""
    xs = np.array([math.log(d["n"]) for d in data_points])
    ys = np.array([math.log(d["top_mag"]) for d in data_points if d["top_mag"] > 0])
    if len(xs) != len(ys) or len(xs) < 3:
        return None
    A = np.vstack([xs, np.ones(len(xs))]).T
    result = np.linalg.lstsq(A, ys, rcond=None)
    slope, intercept = result[0]
    return {"beta": -slope, "intercept": intercept, "r2": 1 - result[1][0] / np.var(ys) / len(ys) if len(result[1]) > 0 else None}


