def find_cube_root_of_unity(p):
    """Return a primitive cube root of 1 in F_p, or None if p ≢ 1 (mod 3)."""
    if p % 3 != 1:
        return None
    for g in range(2, p):
        w = pow(g, (p - 1) // 3, p)
        if w != 1 and pow(w, 3, p) == 1:
            return w
    return None


def aut_index_actions(E_p, G, g_ord, p):
    """Return list of generators of the index action of Aut(E) on
    {0,..,g_ord-1}, as multipliers m ∈ (Z/g_ord)^*.
    [-1] always present (m = -1 mod g_ord).
    [ω] present iff p ≡ 1 (mod 3): then ω·G = (ωGx, Gy), find m s.t. m·G = ω·G.
    """
    multipliers = {1, g_ord - 1}  # identity and [-1]
    w = find_cube_root_of_unity(p)
    if w is not None:
        Gx, Gy = G
        wG = (w * Gx % p, Gy)
        # Search for m so that m·G == wG
        cur = G
        for m in range(1, g_ord):
            if cur == wG:
                multipliers.add(m % g_ord)
                multipliers.add((m * m) % g_ord)  # ω² also
                break
            cur = E_p.add(cur, G)
    return sorted(multipliers)


def orbit_partition(g_ord, multipliers):
    """Compute orbits of {1,..,g_ord-1} under the group generated by
    k → m·k mod g_ord for m in multipliers."""
    seen = [False] * g_ord
    orbits = []
    for k in range(1, g_ord):
        if seen[k]:
            continue
        # BFS
        orb = []
        stack = [k]
        while stack:
            j = stack.pop()
            j %= g_ord
            if j == 0 or seen[j]:
                continue
            seen[j] = True
            orb.append(j)
            for m in multipliers:
                stack.append((m * j) % g_ord)
        orbits.append(orb)
    return orbits


def project_onto_orbit_constants(f_seq, orbits):
    """f_seq is indexed by k = 1..g_ord-1 (length g_ord-1).
    Project onto the subspace of functions constant on each orbit
    (orbit average)."""
    n = len(f_seq)
    proj = np.zeros(n, dtype=complex)
    for orb in orbits:
        # orbit elements are 1..g_ord-1, convert to 0..n-1 indexing
        idxs = [k - 1 for k in orb if 1 <= k <= n]
        if not idxs:
            continue
        avg = sum(f_seq[i] for i in idxs) / len(idxs)
        for i in idxs:
            proj[i] = avg
    return proj


def gowers_with_baseline(g, k_max=3, num_random=200):
    out = {}
    for k in range(2, k_max + 1):
        out[f"U{k}"] = gowers_norm_complex(g, k)
    F = np.fft.fft(g) / len(g)
    out["max_fourier"] = float(np.max(np.abs(F)))
    return out


def run(p, b, e=4, k_max=3, num_random=200):
    E_p = Curve(a=0, b=b, N=p)
    n_pts = naive_order(E_p)
    G, g_ord = find_generator(E_p)
    seq, _, N = z_delta_sequence(p, b, e, max_k=g_ord - 1)
    n = len(seq)
    if n < 8:
        return {"p": p, "status": "too small"}

    multipliers = aut_index_actions(E_p, G, g_ord, p)
    orbits = orbit_partition(g_ord, multipliers)
    aut_size = len(multipliers)

    rand_mean = {}
    rand_std = {}
    for k in range(2, k_max + 1):
        m, s, _ = random_baseline(n, k, num_samples=num_random)
        rand_mean[k] = m
        rand_std[k] = s

    # Baseline for the *projected residual* — apply same projection to
    # random S^1 sequences and measure U^k of the energy-normalized result.
    rand_res_mean = {2: 0.0, 3: 0.0}
    rand_res_std = {2: 0.0, 3: 0.0}
    samples_res = {2: [], 3: []}
    for _ in range(num_random):
        rg = np.exp(1j * np.random.random(n) * 2 * np.pi)
        rproj = project_onto_orbit_constants(rg, orbits)
        rres = rg - rproj
        rms = math.sqrt(float(np.mean(np.abs(rres) ** 2)))
        if rms > 1e-12:
            rres = rres / rms
        for k in (2, 3):
            samples_res[k].append(gowers_norm_complex(rres, k))
    for k in (2, 3):
        arr = np.array(samples_res[k])
        rand_res_mean[k] = float(arr.mean())
        rand_res_std[k] = float(arr.std())

    # Also build a DC-stripped + Aut-stripped baseline
    rand_dc_mean = {2: 0.0, 3: 0.0}
    rand_dc_std = {2: 0.0, 3: 0.0}
    samples_dc = {2: [], 3: []}
    for _ in range(num_random):
        rg = np.exp(1j * np.random.random(n) * 2 * np.pi)
        rg = rg - rg.mean()
        rproj = project_onto_orbit_constants(rg, orbits)
        rres = rg - rproj
        rms = math.sqrt(float(np.mean(np.abs(rres) ** 2)))
        if rms > 1e-12:
            rres = rres / rms
        for k in (2, 3):
            samples_dc[k].append(gowers_norm_complex(rres, k))
    for k in (2, 3):
        arr = np.array(samples_dc[k])
        rand_dc_mean[k] = float(arr.mean())
        rand_dc_std[k] = float(arr.std())

    digit_results = {}
    for j in range(1, e):
        f = np.array([encode_digit(z, p, j) for z in seq], dtype=complex)
        f_meanstrip = f - f.mean()
        proj = project_onto_orbit_constants(f_meanstrip, orbits)
        residual = f_meanstrip - proj
        rms = math.sqrt(float(np.mean(np.abs(residual) ** 2)))
        if rms > 1e-12:
            residual_norm = residual / rms
        else:
            residual_norm = residual

        full_norms = gowers_with_baseline(f, k_max)
        res_norms = gowers_with_baseline(residual_norm, k_max)
        f_mean_abs = float(np.abs(f.mean()))

        digit_results[f"d{j}"] = {
            "f_mean_abs": f_mean_abs,
            "U2_full": full_norms["U2"],
            "U2_residual": res_norms["U2"],
            "U2_full_z": round((full_norms["U2"] - rand_mean[2]) / rand_std[2], 3),
            "U2_residual_z": round((res_norms["U2"] - rand_dc_mean[2]) / rand_dc_std[2], 3),
            "U3_full": full_norms.get("U3"),
            "U3_residual": res_norms.get("U3"),
            "U3_full_z": round((full_norms["U3"] - rand_mean[3]) / rand_std[3], 3),
            "U3_residual_z": round((res_norms["U3"] - rand_dc_mean[3]) / rand_dc_std[3], 3),
            "max_fourier_full": full_norms["max_fourier"],
            "max_fourier_residual": res_norms["max_fourier"],
        }
    return {
        "p": p, "b": b, "n": n, "g_ord": g_ord,
        "aut_multipliers": multipliers,
        "aut_size_on_indices": aut_size,
        "n_orbits": len(orbits),
        "orbit_sizes_first10": [len(o) for o in orbits[:10]],
        "digits": digit_results,
    }


def main():
    out_dir = ROOT / "results"
    out_dir.mkdir(exist_ok=True)
    SECP = [(p, 7) for p in (31, 43, 67, 79, 97, 103, 127, 167, 211,
                              257, 311, 401, 521)]
    reports = []
    for p, b in SECP:
        print(f"[phase37c] p={p} b={b}")
        try:
            r = run(p, b, e=4, k_max=3, num_random=150)
        except Exception as exc:
            import traceback
            traceback.print_exc()
            r = {"p": p, "error": repr(exc)}
        reports.append(r)
        if "digits" in r:
            print(f"   n={r['n']}  aut={r['aut_size_on_indices']}  orbits={r['n_orbits']}")
            for j in (1, 2, 3):
                d = r["digits"][f"d{j}"]
                print(f"   d{j}: |mean|={d['f_mean_abs']:.4f}  "
                      f"U2 full={d['U2_full']:.4f} (z={d['U2_full_z']:+.2f})  "
                      f"DC+Aut-stripped={d['U2_residual']:.4f} (z={d['U2_residual_z']:+.2f})")
        print()
    (out_dir / "phase37c_aut_residual.json").write_text(
        json.dumps(reports, indent=2, default=str))


if __name__ == "__main__":
    main()
