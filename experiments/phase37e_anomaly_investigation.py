"""
Phase 37e — Focused investigation of the degree-1 Fourier anomaly.

Phase 37d established:
  - f_dj(k) = exp(2πi · digit_j(z(δ(k)))/p) has a persistent ~10σ
    U^2 elevation above random, for all digits j=1,2,3 and all primes
    p=31..521 on y²=x³+7.
  - The elevation survives DC strip, Aut-orbit projection, AND random
    index permutation — so it's in the cocycle *order*, not values.
  - U^3 is dead — the structure is degree-1 (linear Fourier) only.

This script investigates:

  1. **Which Fourier mode(s) carry the elevation?**  For each (p, digit j),
     identify the frequency α that maximizes |f̂_dj(α)|. Report α, its
     magnitude, and the ratio to the 2nd-largest.

  2. **Does α correlate with known algebraic invariants?**
     - a_p = p + 1 - #E(F_p)  (trace of Frobenius)
     - g_ord (generator order)
     - π = (a_p + √(a_p² - 4p))/2  (Frobenius eigenvalue in Z[ω])
     - ω_index: index of [ω]G in <G> (the CM automorphism action)
     Check: α/g_ord mod 1, α·something = a_p?, etc.

  3. **Precision dependence:** does the anomaly grow/shrink/persist as
     e increases (e=2,3,4,5,6)?

  4. **CM specificity:** does the anomaly exist on non-j=0 curves?
     Test y²=x³+x (j=1728, CM by Z[i]) and y²=x³+x+1 (generic, no CM).

  5. **Cross-digit correlation:** are the anomalous Fourier modes at the
     same frequency α across digits j=1,2,3?

  6. **Scaling law:** fit the peak Fourier magnitude vs n to determine
     if |f̂(α)| ~ n^{-β} for β < 1/2 (real signal) or β = 1/2 (random).
"""
