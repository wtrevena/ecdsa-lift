"""
Phase 9 — Lattice side: can LLL on a p-adic representation of ECDLP
recover the discrete log directly?

Motivation. Phases 1–8 all target algebraic structure in the lift. A
different angle: treat the pair (τ(G), τ(kG)) as points in (Z/p^e)³
and look for a short integer vector relating them. This is the same
philosophy as "lattice attacks on DSA with partial nonces" (Howgrave-
Graham / Smart / Boneh-Venkatesan) but applied to the lift itself.

Construction. For a sample of scalars k_1, ..., k_m, form vectors
    v_i  =  (k_i,  X_i,  Y_i,  Z_i)
where (X_i, Y_i, Z_i) are the projective coordinates of τ(k_i·G),
reduced mod p^e. If there is ANY Z-linear relation among these (short
relative to p^e), LLL on the lattice generated by the v_i plus the
standard basis vectors scaled by p^e will find it.

A successful non-trivial short vector would mean (X_i, Y_i, Z_i)
contains linear leakage about k_i — an exploitable structural flaw.

Expected: no short vector. Reason: Phase 1 showed the lift is
injective but the image looks pseudo-random in Ê.
"""
from __future__ import annotations
import json
import sys
import pathlib
import random

ROOT = pathlib.Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))

from src.curves import Curve, naive_order, find_generator
from src.projective import ProjCurve
from src.lifts import teichmuller_lift
from src.lll import lll


def tau_proj(E_p, C, P):
    E_aff = Curve(a=C.a, b=C.b, N=C.N)
    return C.from_affine(teichmuller_lift(E_p, E_aff, P))


def run(p: int, b: int, e: int = 3, m: int = 6) -> dict:
    E_p = Curve(a=0, b=b, N=p)
    n = naive_order(E_p)
    if n % p == 0:
        return {"prime": p, "status": "anomalous"}
    G, g_ord = find_generator(E_p)
    if g_ord < m + 4:
        return {"prime": p, "status": "tiny"}
    N = p ** e
    C = ProjCurve(a=0, b=b, N=N)

    random.seed(p)
    ks = sorted(random.sample(range(2, g_ord), m))
    samples = []
    for k in ks:
        P = E_p.mul(k, G)
        X, Y, Z = tau_proj(E_p, C, P)
        samples.append((k, X % N, Y % N, Z % N))

    # Lattice. Rows encode:
    #   [k_i, X_i, Y_i, Z_i, 0...0]       (one per sample, to be combined)
    #   [0,   N,   0,   0,   0...]        (modular reduction for X coord)
    #   [0,   0,   N,   0,   0...]
    #   [0,   0,   0,   N,   0...]
    # Scaling: put weight W on the k column so we favor small-k solutions.
    W = N  # strongest scaling; short vector must have small k component
    rows = []
    for (k, X, Y, Z) in samples:
        rows.append([W * k, X, Y, Z])
    rows.append([0, N, 0, 0])
    rows.append([0, 0, N, 0])
    rows.append([0, 0, 0, N])
    reduced = lll(rows)
    reduced_sorted = sorted(reduced, key=lambda r: sum(x * x for x in r))
    shortest = reduced_sorted[0]
    # A "win" would be a vector whose k-component divided by W is a
    # known small integer, with the Y/X/Z part = 0 (meaning: some linear
    # combination of sample τ(k_iG) equals 0 in projective coordinates).
    k_component = shortest[0] // W if W else 0
    rest = shortest[1:]
    trivial = all(x == 0 for x in shortest)

    return {
        "prime": p, "curve_b": b, "g_ord": g_ord, "N": N,
        "samples": [(k, X, Y, Z) for (k, X, Y, Z) in samples],
        "shortest_vector": shortest,
        "shortest_norm_sq": sum(x * x for x in shortest),
        "k_component_if_interpretable": k_component,
        "rest_all_zero": all(x == 0 for x in rest),
        "trivial_zero_vector": trivial,
        "verdict": ("structural leak" if (not trivial and all(x == 0 for x in rest)
                                          and abs(k_component) < 2 ** 16)
                    else "no linear structure in lift"),
    }


def main():
    candidates = [
        (31, 3), (43, 7), (67, 2), (73, 13),
        (79, 3), (97, 5), (103, 5),
    ]
    out_dir = ROOT / "results"
    out_dir.mkdir(exist_ok=True)
    for p, b in candidates:
        print(f"[phase9] p={p} b={b}")
        try:
            report = run(p, b)
        except Exception as exc:
            report = {"prime": p, "error": repr(exc)}
        (out_dir / f"phase9_p{p}.json").write_text(
            json.dumps(report, indent=2, default=str))
        print(f"   verdict: {report.get('verdict', report.get('status'))}")
        if "shortest_norm_sq" in report:
            print(f"   shortest_norm_sq: {report['shortest_norm_sq']}")


if __name__ == "__main__":
    main()
