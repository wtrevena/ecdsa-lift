"""
Phase 14d — Hypothesis: the Phase 14b/c rank deficit is Aut(E) acting.

For j = 0 curves y² = x³ + b, Aut(E) = μ₆, generated by
    (x, y) → (ζ x, -y),  ζ³ = 1 in F_p.
On points of prime order n, this corresponds to a subgroup of (Z/n)*
of order 6: {1, λ, λ², -1, -λ, -λ²} where λ² + λ + 1 ≡ 0 mod n.

Hypothesis: the leading-digit row of δ depends only on the μ₆-orbit
of H, up to an F_p-scalar that's a character of μ₆.
  • Each orbit of length 6 contributes rank 1.
  • Number of non-trivial orbits = (g_ord - 1) / 6.
  • Observed rank from Phase 14c = (g_ord - 1) / 6 exactly.

Test:
  (1) Find ζ (cube root of 1 in F_p).
  (2) For each H_i = iG, i ∈ [1, g_ord-1], compute ζ·H_i via the curve
      action (x,y) → (ζx, -y) wait actually need to think — the
      *induced* action on (Z/n)* is multiplication by some λ with
      λ²+λ+1=0 mod n (ζ acts as CM).
  (3) Group the 1..g_ord-1 into orbits under ⟨λ, -1⟩ ⊂ (Z/n)*.
  (4) For each orbit, check that all (δ-rows / p mod p) are F_p-scalar
      multiples of a single direction vector.

If (4) holds across all orbits, the Phase 14b/c rank deficit is
completely explained by Aut(E), and there is no residual structure.
"""
from __future__ import annotations
import json
import sys
import pathlib

ROOT = pathlib.Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))

from src.curves import Curve, naive_order, find_generator, modinv
from src.projective import ProjCurve
from src.lifts import teichmuller_lift


def delta_H_k_leading(E_p, C, p, H, k):
    """(δ_H(k) / p) mod p — the leading p-adic digit."""
    E_aff = Curve(a=C.a, b=C.b, N=C.N)
    kH = E_p.mul(k, H)
    tau_H = C.from_affine(teichmuller_lift(E_p, E_aff, H))
    tau_kH = C.from_affine(teichmuller_lift(E_p, E_aff, kH))
    diff = C.add(C.mul(k, tau_H), C.neg(tau_kH))
    X, Y, Z = diff
    try:
        v = (-X * modinv(Y % C.N, C.N)) % C.N
    except ZeroDivisionError:
        return 0
    return (v // p) % p


def find_lambda(n):
    """Find λ ∈ (Z/n)* with λ² + λ + 1 ≡ 0 mod n. Returns None if none."""
    for lam in range(2, n):
        if (lam * lam + lam + 1) % n == 0:
            return lam
    return None


def compute_orbits(n, lam):
    """Group {1, ..., n-1} into orbits under the action of
    ⟨λ, -1⟩ ⊂ (Z/n)*. Each orbit is a μ₆-orbit in generic cases."""
    seen = set()
    orbits = []
    for start in range(1, n):
        if start in seen:
            continue
        orbit = set()
        stack = [start]
        while stack:
            x = stack.pop()
            if x in orbit:
                continue
            orbit.add(x)
            for nxt in [(lam * x) % n, (-x) % n]:
                if nxt != 0 and nxt not in orbit:
                    stack.append(nxt)
        orbits.append(sorted(orbit))
        seen |= orbit
    return orbits


def rows_proportional(row_a, row_b, p):
    """Check if row_b = c * row_a mod p for some c ∈ F_p^*.
    Returns c if proportional, else None."""
    cols = len(row_a)
    # Find first nonzero col in row_a
    c = None
    for j in range(cols):
        if row_a[j] % p != 0:
            if c is None:
                c = (row_b[j] * pow(row_a[j], -1, p)) % p
            else:
                pass
            break
    if c is None:
        # row_a is zero; row_b must also be zero
        return 0 if all(x % p == 0 for x in row_b) else None
    for j in range(cols):
        if (row_b[j] - c * row_a[j]) % p != 0:
            return None
    return c


def run(p, b, e=3, size=None):
    E_p = Curve(a=0, b=b, N=p)
    n = naive_order(E_p)
    if n % p == 0:
        return {"prime": p, "status": "anomalous"}
    G, g_ord = find_generator(E_p)
    if size is None:
        size = g_ord - 1
    if g_ord < 8:
        return {"prime": p, "status": "small"}
    lam = find_lambda(g_ord)
    if lam is None:
        return {"prime": p, "status": "no lambda",
                "g_ord": g_ord}
    N = p ** e
    C = ProjCurve(a=0, b=b, N=N)

    # Build rows for H_i = iG, i = 1..g_ord-1
    # Columns k = 2..size+1 (avoid trivial k=1)
    ks = list(range(2, size + 1))
    rows = {}
    for i in range(1, g_ord):
        H = E_p.mul(i, G)
        rows[i] = [delta_H_k_leading(E_p, C, p, H, k) for k in ks]

    orbits = compute_orbits(g_ord, lam)
    # Exclude any orbit containing 0 (there shouldn't be any)
    orbits = [o for o in orbits if o and o[0] != 0]

    # For each orbit, check all rows are proportional to the first
    orbit_reports = []
    all_proportional = True
    for orbit in orbits:
        base_i = orbit[0]
        base = rows[base_i]
        scalars = {}
        ok = True
        for i in orbit:
            c = rows_proportional(base, rows[i], p)
            if c is None:
                ok = False
                break
            scalars[i] = c
        if not ok:
            all_proportional = False
        orbit_reports.append({
            "orbit": orbit,
            "proportional": ok,
            "scalars_sample": list(scalars.items())[:6],
        })

    # Verify rank prediction
    num_nontrivial_orbits = len([o for o in orbits if len(o) > 0])
    expected_rank = num_nontrivial_orbits

    return {
        "prime": p, "curve_b": b, "g_ord": g_ord,
        "lambda_modn": lam,
        "num_orbits": len(orbits),
        "orbit_sizes": sorted(set(len(o) for o in orbits)),
        "orbit_size_histogram": {
            s: sum(1 for o in orbits if len(o) == s)
            for s in sorted(set(len(o) for o in orbits))},
        "all_orbits_proportional": all_proportional,
        "expected_rank": expected_rank,
        "verdict": (
            "CONFIRMED: leading-digit rank = Aut(E)-orbit count"
            if all_proportional else
            "REJECTED: some orbit rows not F_p-proportional"),
        "first_orbit_scalars": orbit_reports[0] if orbit_reports else None,
    }


def main():
    candidates = [
        (31, 3), (43, 7), (67, 2), (73, 13),
        (79, 3), (97, 5), (103, 5),
    ]
    out_dir = ROOT / "results"
    out_dir.mkdir(exist_ok=True)
    for p, b in candidates:
        print(f"[phase14d] p={p} b={b}")
        try:
            report = run(p, b)
        except Exception as exc:
            report = {"prime": p, "error": repr(exc)}
        (out_dir / f"phase14d_p{p}.json").write_text(
            json.dumps(report, indent=2, default=str))
        for k, v in report.items():
            print(f"   {k}: {v}")
        print()


if __name__ == "__main__":
    main()
